namespace Loom.Collections
{
    /// <summary>
    ///     Enumeration to determine sorting direction.
    /// </summary>
    public enum SortDirection
    {
        /// <summary>
        ///     No sorting
        /// </summary>
        None = 0,

        /// <summary>
        ///     Sort Ascending
        /// </summary>
        Ascending = 1,

        /// <summary>
        ///     Sort Descending
        /// </summary>
        Descending = 2
    }
}